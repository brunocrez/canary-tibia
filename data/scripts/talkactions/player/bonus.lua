function bonus.onSay(player, words, param)
	local outfitCount = 0
	local mountCount = 0
	for i = 1, #lookTypes do
		if player:hasOutfit(lookTypes[i], 3) then
			outfitCount = outfitCount + 1
		end
	end

	for i = 1, #mounts do
		if player:hasMount(mounts[i]) then
			mountCount = mountCount + 1
		end
	end

	local text = "Total Outfits: " .. outfitCount .. " (+" .. outfitCount .. " Fist Fighting)"
					.. "\nTotal Mounts: " .. mountCount .. " (+" .. math.floor(mountCount / 5) .. " Fist Fighting)"
	player:popupFYI(text)
	return true
end

